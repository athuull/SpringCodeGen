public class MathOperations {
  public static double square(double num) {
    return num * num;
  }

  public static double cube(double num) {
    return num * num * num;
  }

  public static double sqrt(double num) {
    return Math.sqrt(num);
  }

  public static double abs(double num) {
    return Math.abs(num);
  }
}